// Expression untuk membandingkan sesuatu kondisi
// const => untuk mendeklarasikan sesuatu variable yang sifatnya tidak bisa diganti
// let => untuk mendeklarasikan sesuatu variable yang sifatnya bisa diganti

// assignment
let a = 1;
a = 2;
// console.log("hasilnya: ", a);
const b = 3;

// addition
a = a + b;
// console.log("hasilnya: ", a);
a += b;
// console.log("hasilnya: ", a);

// Multiplication assignment
let x = 2;
const y = 5;
x = x * y;
x *= y;
console.log("hasilnya: ", x);
