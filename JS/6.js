// Comparison operators => untuk membandingkan sesuatu value yang ada

// const a = 1;
const b = 2;
const c = "2";
const result = b == c; // membandingkan 2 value yang mengabaikan tipe data
const result2 = b === c; // membandingkan 2 value yang mementingkan tipe data
const result3 = b != c; // membandingkan 2 value tidak sama yang mementingkan tipe data
const result4 = b !== c; // membandingkan 2 value  tidak sama  mementingkan tipe data
console.log("hasilnya: ", result);
console.log("hasilnya2: ", result2);
console.log("hasilnya3: ", result3);
console.log("hasilnya3: ", result4);
